from datetime import date


def days_diff(a, b):
    # your code here
    return abs((date(a[0], a[1], a[2]) - date(b[0], b[1], b[2])).days)


if __name__ == "__main__":
    print("Example:")
    print(days_diff((1982, 4, 19), (1982, 4, 22)))

